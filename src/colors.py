"""Module containing ANSI escape sequences for terminal text coloring.

This module provides a set of string constants to format terminal output
with various colors and styles (bold, reset).
"""

# ANSI Escape Sequences for Colors
CYAN = "\033[96m"
GREEN = "\033[92m"
YELLOW = "\033[93m"
RED = "\033[91m"
RESET = "\033[0m"
BOLD = "\033[1m"
MAGENTA = "\033[95m"
WHITE = "\033[97m"
BLACK = "\033[30m"
BLUE = "\033[94m"
GREY = "\033[90m"
